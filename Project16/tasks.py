from crewai import Task
from agents import researcher, writer, editor

# Задача 1: Исследование
research_task = Task(
    description=(
        'Провести глубокое исследование по теме: "{topic}". '
        'Собрать минимум 5 ключевых фактов, актуальную статистику и ссылки на авторитетные источники. '
        'Обратить внимание на последние тренды и новости в этой сфере.'
    ),
    expected_output='Структурированный исследовательский бриф с фактами, цифрами и ссылками на источники.',
    agent=researcher
)

# Задача 2: Написание черновика
write_task = Task(
    description=(
        'Используя исследовательский бриф, написать полноценную статью (800-1200 слов) по теме: "{topic}". '
        'Статья должна иметь цепляющий заголовок, введение, несколько логичных разделов с подзаголовками и заключение. '
        'Текст должен быть написан живым, экспертным языком.'
    ),
    expected_output='Готовый черновик статьи в формате Markdown, разбитый на смысловые блоки.',
    agent=writer
)

# Задача 3: Редактура и сохранение
edit_task = Task(
    description=(
        'Проверить черновик статьи на орфографические, пунктуационные и стилистические ошибки. '
        'Убедиться, что текст звучит естественно, профессионально и легко читается. '
        'Удалить "воду", проверить логику повествования.'
    ),
    expected_output='Финальная, отполированная статья в формате Markdown, полностью готовая к публикации.',
    agent=editor,
    output_file='output/final_article.md'
)