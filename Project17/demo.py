from agent import agent_executor

# Перенесли промпт сюда
system_prompt = """Ты — умный и вежливый персональный ИИ-ассистент. 
У тебя есть доступ к реальным данным через внешние инструменты (погода и новости).
Твоя задача: внимательно проанализировать запрос пользователя и решить, какие инструменты нужны для ответа. 
Если запрос сложный, ты можешь вызывать несколько инструментов по очереди.
Формируй финальный ответ на русском языке, ясно, структурированно и опираясь ТОЛЬКО на данные, полученные от инструментов. 
Если инструмент вернул ошибку или ничего не нашел, честно и вежливо сообщи об этом пользователю."""

def main():
    print("🤖 Запуск ИИ-агента с реальными инструментами (Проект 17)\n" + "="*60)
    
    test_queries = [
        "Какая сейчас погода в Москве?",
        "Какие последние новости про искусственный интеллект?",
        "Какая сейчас погода в Алматы и есть ли какие-нибудь важные новости о Казахстане?",
        "Стоит ли мне ехать в Стамбул на этой неделе? Какая там погода и что там происходит?",
        "Какая погода в городе Несуществуйскград?"
    ]

    for i, query in enumerate(test_queries, 1):
        print(f"\n[{i}/5] 👤 Пользователь: {query}")
        print("-" * 60)
        
        try:
            # Передаем системный промпт первым сообщением, а запрос пользователя - вторым.
            # Эта структура железобетонная и работает в любой версии LangGraph!
            response = agent_executor.invoke({
                "messages": [
                    ("system", system_prompt),
                    ("user", query)
                ]
            })
            
            # Финальный ответ агента хранится в самом последнем сообщении
            final_message = response["messages"][-1].content
            
            print("\n✅ Финальный ответ агента:")
            print(final_message)
        except Exception as e:
            print(f"\n❌ Произошла ошибка при выполнении: {e}")
        
        print("=" * 60)

if __name__ == "__main__":
    main()